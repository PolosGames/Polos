#	define SAVE_PROFILING(...)
#	define PROFILE_SCOPE(SCOPE_NAME)
#	define PROFILE_FRAME(FRAME_NAME, ...)
#	define PROFILE_THREAD(THREAD_NAME)
#	define PROFILE_FUNC(...)
#	define PROFILE_TAG(TAG_NAME, ...)
#endif
